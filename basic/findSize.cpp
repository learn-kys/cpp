#include <bits/stdc++.h>
using namespace std;
int main() {
    int n  = 5;
    float flt = 3.5;
    bool b = true;
    char c = 'c';
    double d = 23.2;

    cout << "size of integer " << sizeof(n) << endl;
    cout << "size of float " << sizeof(flt) << endl;
    cout << "size of boolean " << sizeof(b) << endl;
    cout << "size of character " << sizeof(c) << endl;
    cout << "size of double " << sizeof(d) << endl;
}